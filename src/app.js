"use strict";
window.addEventListener("scroll", function () {
    var listNode = document.querySelectorAll(".tecnologies__card-1") ||
        document.createElement("div");
    var firstElement = listNode[0];
    var position = firstElement.getBoundingClientRect().top;
    var tamanioPantalla = window.innerHeight / 2;
    for (var i = 0; i < listNode.length; i++) {
        var element = listNode[i];
        if (position < tamanioPantalla) {
            element.classList.add("tecnologies__fade_in");
            element.classList.remove("tecnologies__fade_out");
        }
        else {
            element.classList.remove("tecnologies__fade_in");
            element.classList.add("tecnologies__fade_out");
        }
    }
});
